import pandas as pd
from .substract import main


def test_numeric():
    assert main(a=17.5, b=16.25)["difference"] == 1.25


def test_series_numeric():
    assert main(
        a=pd.Series(
            {
                "2019-08-01T15:20:12": 1.2,
                "2019-08-01T15:44:12": 4,
                "2019-08-03T16:20:15": 0.3,
                "2019-08-05T12:00:34": 0.5,
            }
        ),
        b=-6,
    )["difference"].equals(
        pd.Series(
            {
                "2019-08-01T15:20:12": 7.2,
                "2019-08-01T15:44:12": 10,
                "2019-08-03T16:20:15": 6.3,
                "2019-08-05T12:00:34": 6.5,
            }
        )
    )


